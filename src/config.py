# ==========================================
# RAGシステム 統合設定ファイル
# ==========================================
# このファイルを編集することで、システム全体の挙動を一括で調整できます。

# ------------------------------------------
# 1. LLM (回答生成) 設定
# ------------------------------------------
import os
from dotenv import load_dotenv

# .envファイルを読み込む
load_dotenv()

# Ollama (回答生成) 設定
OLLAMA_BASE_URL = os.environ.get("OLLAMA_BASE_URL")
OLLAMA_MODEL_NAME = os.environ.get("OLLAMA_MODEL_NAME")

# 回答のランダム性 (0.0に近いほど事実重視、1.0に近いほど創造的になるが嘘が混じりやすい)
LLM_TEMPERATURE = 0.3

# LLMが生成できる最大文字数(トークン数)
LLM_MAX_NEW_TOKENS = 5120000

# ------------------------------------------
# 2. 検索 (Retriever) 設定
# ------------------------------------------
# LLMの回答生成用に渡す参考資料（チャンク）の最大件数
RETRIEVER_LIMIT = 5

# Qdrant Cloud上のデータベース（コレクション）名
COLLECTION_NAME = "article_chunks"

# ------------------------------------------
# 3. インデクシング (Chunking) 設定
# ------------------------------------------
# Notionのテキストを何文字ごとに分割するか
CHUNK_SIZE = 400

# 分割する際に、前後の文脈が途切れないように何文字重複させるか
CHUNK_OVERLAP = 50

# ------------------------------------------
# 4. エンベディング (Embedding) 設定
# ------------------------------------------
# 意味検索(Dense)に使用するエンベディングモデル
DENSE_EMBEDDING_MODEL = "intfloat/multilingual-e5-base"

# エンベディングモデルの出力次元数（e5-baseは768）
DENSE_VECTOR_SIZE = 768

# エンベディングモデルの最大入力長（e5-baseは512）
EMBEDDING_MAX_LENGTH = 512

# ------------------------------------------
# 5. セッション (Session) 設定
# ------------------------------------------
# セッション履歴を保存するディレクトリ
SESSIONS_DIR = "data/sessions"
